def tune_nb(X_train_text, y_train, X_dev_text, y_dev):
    best = None
    all_rows = []

    keys = list(NB_PARAM_GRID.keys())
    values = [NB_PARAM_GRID[k] for k in keys]

    for combo in product(*values):
        params = dict(zip(keys, combo))

        vectorizer = build_vectorizer(
            ngram_range=params["ngram_range"],
            min_df=params["min_df"],
            max_df=params["max_df"],
        )

        X_train_vec = vectorizer.fit_transform(X_train_text)
        X_dev_vec = vectorizer.transform(X_dev_text)

        clf = MultinomialNB(alpha=params["alpha"])
        clf.fit(X_train_vec, y_train)
        dev_preds = clf.predict(X_dev_vec)

        metrics = evaluate_predictions(y_dev, dev_preds)
        row = {
            "model": "MultinomialNB",
            **params,
            **metrics,
        }
        all_rows.append(row)

        if best is None or metrics["macro_f1"] > best["dev_macro_f1"]:
            best = {
                "vectorizer": vectorizer,
                "clf": clf,
                "params": params,
                "dev_preds": dev_preds,
                "dev_macro_f1": metrics["macro_f1"],
                "dev_metrics": metrics,
            }

    return best, pd.DataFrame(all_rows)


def tune_lr(X_train_text, y_train, X_dev_text, y_dev):
    best = None
    all_rows = []

    keys = list(LR_PARAM_GRID.keys())
    values = [LR_PARAM_GRID[k] for k in keys]

    for combo in product(*values):
        params = dict(zip(keys, combo))

        vectorizer = build_vectorizer(
            ngram_range=params["ngram_range"],
            min_df=params["min_df"],
            max_df=params["max_df"],
        )

        X_train_vec = vectorizer.fit_transform(X_train_text)
        X_dev_vec = vectorizer.transform(X_dev_text)

        clf = LogisticRegression(
            C=params["C"],
            class_weight=params["class_weight"],
            max_iter=4000,
            solver="lbfgs",
            random_state=RANDOM_STATE,
        )

        clf.fit(X_train_vec, y_train)
        dev_preds = clf.predict(X_dev_vec)

        metrics = evaluate_predictions(y_dev, dev_preds)
        row = {
            "model": "LogisticRegression",
            **params,
            **metrics,
        }
        all_rows.append(row)

        if best is None or metrics["macro_f1"] > best["dev_macro_f1"]:
            best = {
                "vectorizer": vectorizer,
                "clf": clf,
                "params": params,
                "dev_preds": dev_preds,
                "dev_macro_f1": metrics["macro_f1"],
                "dev_metrics": metrics,
            }

    return best, pd.DataFrame(all_rows)


def refit_and_test(vectorizer_params, model_name, model_params,
                   X_train_text, y_train, X_dev_text, y_dev, X_test_text):
    X_train_dev_text = pd.concat([X_train_text, X_dev_text], axis=0)
    y_train_dev = pd.concat([y_train, y_dev], axis=0)

    vectorizer = build_vectorizer(
        ngram_range=vectorizer_params["ngram_range"],
        min_df=vectorizer_params["min_df"],
        max_df=vectorizer_params["max_df"],
    )

    X_train_dev_vec = vectorizer.fit_transform(X_train_dev_text)
    X_test_vec = vectorizer.transform(X_test_text)

    if model_name == "MultinomialNB":
        clf = MultinomialNB(alpha=model_params["alpha"])
    elif model_name == "LogisticRegression":
        clf = LogisticRegression(
            C=model_params["C"],
            class_weight=model_params["class_weight"],
            max_iter=4000,
            solver="lbfgs",
            random_state=RANDOM_STATE,
        )
    else:
        raise ValueError(f"Unknown model_name: {model_name}")

    clf.fit(X_train_dev_vec, y_train_dev)
    test_preds = clf.predict(X_test_vec)

    return vectorizer, clf, test_preds


def extract_top_lr_features(vectorizer, clf, top_n=25):
    feature_names = vectorizer.get_feature_names_out()
    coef_matrix = clf.coef_
    classes = clf.classes_

    rows = []
    for i, class_label in enumerate(classes):
        top_idx = np.argsort(coef_matrix[i])[-top_n:][::-1]
        for rank, idx in enumerate(top_idx, start=1):
            rows.append({
                "class": class_label,
                "rank": rank,
                "feature": feature_names[idx],
                "coefficient": float(coef_matrix[i][idx]),
            })
    return pd.DataFrame(rows)


