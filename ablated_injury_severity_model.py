def print_header(title: str) -> None:
    print("\n" + "=" * 78)
    print(title)
    print("=" * 78)


def normalize_text(text: str) -> str:
    text = str(text).lower()
    text = re.sub(r"[/\-]", " ", text)
    text = re.sub(r"[^a-z0-9\s]", " ", text)
    text = re.sub(r"\s+", " ", text).strip()
    return text


def remove_leakage_terms(text: str) -> str:
    text = normalize_text(text)
    text = LEAKAGE_REGEX.sub(" ", text)
    text = re.sub(r"\s+", " ", text).strip()
    return text


def load_and_prepare_split(path: str) -> pd.DataFrame:
    df = pd.read_csv(path)

    required_cols = ["input_text", "severity"]
    missing = [c for c in required_cols if c not in df.columns]
    if missing:
        raise ValueError(f"{path} is missing required columns: {missing}")

    df = df.copy()
    df["severity"] = df["severity"].astype(str).str.strip().str.upper()
    df = df[df["severity"].isin(VALID_LABELS)].copy()
    df["model_text"] = df["input_text"].fillna("").astype(str).apply(remove_leakage_terms)

    return df


def show_sample_ablation(df: pd.DataFrame, n: int = 6) -> None:
    print_header("Sample Ablated Rows")
    sample = df.head(n).copy()
    for i, row in sample.iterrows():
        print(f"\nRow {i}")
        print("Original:", row["input_text"])
        print("Ablated: ", row["model_text"])


def build_vectorizer(ngram_range=(1, 2), min_df=2, max_df=0.98):
    return TfidfVectorizer(
        ngram_range=ngram_range,
        min_df=min_df,
        max_df=max_df,
        sublinear_tf=True,
    )


def evaluate_predictions(y_true, y_pred) -> dict:
    macro_f1 = f1_score(y_true, y_pred, average="macro")
    weighted_f1 = f1_score(y_true, y_pred, average="weighted")
    acc = accuracy_score(y_true, y_pred)

    prec, rec, f1s, supports = precision_recall_fscore_support(
        y_true,
        y_pred,
        labels=VALID_LABELS,
        zero_division=0
    )

    return {
        "accuracy": acc,
        "macro_f1": macro_f1,
        "weighted_f1": weighted_f1,
        "precision_minor": prec[0],
        "recall_minor": rec[0],
        "f1_minor": f1s[0],
        "support_minor": supports[0],
        "precision_moderate": prec[1],
        "recall_moderate": rec[1],
        "f1_moderate": f1s[1],
        "support_moderate": supports[1],
        "precision_severe": prec[2],
        "recall_severe": rec[2],
        "f1_severe": f1s[2],
        "support_severe": supports[2],
    }


def confusion_to_long_df(y_true, y_pred, model_name, split_name):
    cm = confusion_matrix(y_true, y_pred, labels=VALID_LABELS)
    rows = []
    for i, true_label in enumerate(VALID_LABELS):
        for j, pred_label in enumerate(VALID_LABELS):
            rows.append({
                "model": model_name,
                "split": split_name,
                "true_label": true_label,
                "pred_label": pred_label,
                "count": int(cm[i, j]),
            })
    return pd.DataFrame(rows)


def print_eval_block(title: str, y_true, y_pred):
    print_header(title)
    metrics = evaluate_predictions(y_true, y_pred)
    print(f"Accuracy:  {metrics['accuracy']:.4f}")
    print(f"Macro F1:  {metrics['macro_f1']:.4f}")
    print("\nClassification Report:")
    print(classification_report(y_true, y_pred, digits=4, zero_division=0))
    print("Confusion Matrix:")
    print(confusion_matrix(y_true, y_pred, labels=VALID_LABELS))


