LR_PARAM_GRID = {
    "ngram_range": [(1, 1), (1, 2)],
    "min_df": [1, 2, 5],
    "max_df": [0.95, 0.98],
    "C": [0.1, 1.0, 3.0, 10.0],
    "class_weight": [None, "balanced"],
}

NB_PARAM_GRID = {
    "ngram_range": [(1, 1), (1, 2)],
    "min_df": [1, 2, 5],
    "max_df": [0.95, 0.98],
    "alpha": [0.25, 0.5, 1.0],
}


